    <h3>Apa itu Framework??</h3>
    <p> Framework adalah sebuah kerangka kerja yang digunakan untuk mempermudah para developer software dalam membuat dan mengembangkan aplikasi. Framework berisikan perintah dan fungsi dasar yang umum digunakan untuk membangun sebuah software aplikasi sehingga diharapkan aplikasi dapat dibangun dengan lebih cepat serta tersusun dan terstruktur dengan cukup rapi. Framework juga bisa diartikan sebagai komponen – komponen pemrograman yang sudah jadi dan siap untuk digunakan kapan saja, sehingga pengembang aplikasi tidak perlu lagi membuat scrip yang sama untuk tugas – tugas yang sama.</p>
    <p> Ada dua jenis Framework dalam dunia pemrograman yaitu Desktop Framework dan Web Framework, keduanya memiliki fungsi yang berbeda, Desktop Framewok digunakan untuk membangun aplikasi berbasis desktop sedangkan Web Framewok digunakan untuk membangun aplikasi berbasis web.</p>
